// ─── Main export ─────────────────────────────────────────────────────────────

export function generateOfflineContent(keyword: KeywordConfig, config: ProjectConfig): PostData {
  const title = generateTitle(keyword, config);
  const slug = slugify(title);
  const description = generateDescription(keyword, config);
  const faqs = generateFaqs(keyword, config);
  const body = generateBody(keyword, config, faqs);

  // Insert internal links
  const bodyWithLinks = insertInternalLinks(body, config.internalLinkTargets, keyword);

  // Add E-E-A-T block and CTA
  const eeat = generateEeatBlock(config);
  const cta = generateCta(config, keyword);
  const fullBody = bodyWithLinks + '\n\n' + eeat + '\n\n' + cta;

  const wordCount = countWords(fullBody);
  const readingTime = readingTimeMins(wordCount);

  // Build tags from cluster and keyword
  const tags = [keyword.cluster, config.primaryCategory];
  if (keyword.intent === 'commercial' || keyword.intent === 'transactional') {
    tags.push('خرید');
  }

  return {
    title,
    slug,
    description,
    date: todayIso(),
    author: config.authorName,
    tags,
    keywords: [keyword.keyword, ...keyword.keyword.split(' ').slice(0, 3)],
    canonicalUrl: `${config.siteUrl}/blog/${slug}`,
    readingTime,
    faqs,
    body: fullBody,
  };
}