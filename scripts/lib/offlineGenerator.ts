// ─── CTA block ───────────────────────────────────────────────────────────────

function generateCta(config: ProjectConfig, keyword: KeywordConfig): string {
  const productLinks = config.internalLinkTargets.filter(t => t.type === 'product' || t.type === 'category');
  const contactLink = config.internalLinkTargets.find(t => t.url.includes('contact'));
  const guideLink = config.internalLinkTargets.find(t => t.type === 'guide');

  const lines: string[] = [];
  lines.push('---');
  lines.push('');
  lines.push(`## آماده شروع هستید؟`);
  lines.push('');
  lines.push(`اگر درباره **${keyword.keyword}** سوالی دارید یا آماده اقدام هستید، ما اینجا هستیم تا کمک کنیم:`);
  lines.push('');

  if (productLinks.length > 0) {
    const link = productLinks[0];
    lines.push(`- 🛒 [مشاهده و خرید محصولات](${link.url}) — محصولات اصل با گارانتی اصالت`);
  }
  if (guideLink) {
    lines.push(`- 📖 [راهنمای جامع انتخاب](${guideLink.url}) — برای تصمیم بهتر`);
  }
  if (contactLink) {
    lines.push(`- 📞 [تماس با کارشناسان ما](${contactLink.url}) — مشاوره رایگان`);
  }

  lines.push('');
  lines.push(`**${config.brandName}** — مرجع تخصصی ${config.primaryCategory}`);
  lines.push('');

  return lines.join('\n');
}

// ─── Internal links insertion ───────────────────────────────────────────────

function insertInternalLinks(body: string, targets: InternalLinkTarget[], keyword: KeywordConfig): string {
  // Insert 2-4 natural internal links into the body
  const suitableTargets = targets.filter(t => {
    // Don't link to the exact keyword match
    return !t.title.includes(keyword.keyword.slice(0, 4));
  });

  const selected = pickRandom(suitableTargets, Math.min(3, suitableTargets.length));

  for (const target of selected) {
    // Find a natural place to insert the link
    const anchor = `[${target.title}](${target.url})`;
    const phrasePatterns = [
      `برای اطلاعات بیشتر می‌توانید به ${anchor} مراجعه کنید.`,
      `پیشنهاد می‌کنیم ${anchor} را نیز مطالعه فرمایید.`,
      `اطلاعات تکمیلی در ${anchor} موجود است.`,
    ];

    const pattern = phrasePatterns[Math.floor(Math.random() * phrasePatterns.length)];

    // Insert before the last ## heading or CTA
    const lastHeadingIdx = body.lastIndexOf('\n## ');
    if (lastHeadingIdx > 0) {
      body = body.slice(0, lastHeadingIdx) + '\n\n' + pattern + '\n\n' + body.slice(lastHeadingIdx);
    } else {
      body += '\n\n' + pattern;
    }
  }

  return body;
}

